def check_essay(text: str) -> str:
    """Быстрая самопроверка вставленного сочинения: объем + чек-лист."""
    words = len([w for w in text.split() if any(ch.isalpha() for ch in w)])
    lines = ["📝 <b>Проверка сочинения</b>", "", f"Слов: <b>{words}</b>."]
    if words >= MIN_WORDS:
        lines.append(f"✅ Объем в норме (нужно ≥ {MIN_WORDS}).")
    else:
        lines.append(f"🔴 Мало: нужно ≥ {MIN_WORDS} слов, иначе работа - 0 баллов по всем критериям.")
    lines.extend([
        "",
        "Бот не выставляет балл - проверь себя по критериям:",
        CHECKLIST,
        "<i>Точную оценку по К1-К10 даст преподаватель.</i>",
    ])
    return "\n".join(lines)
