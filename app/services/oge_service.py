def check_text(text: str) -> str:
    """Проверка объема изложения/сочинения ОГЭ (>= 70 слов)."""
    words = len([w for w in text.split() if any(ch.isalpha() for ch in w)])
    lines = ["📝 <b>Проверка объема (ОГЭ)</b>", "", f"Слов: <b>{words}</b>."]
    if words >= MIN_WORDS:
        lines.append(f"✅ Объем в норме (нужно ≥ {MIN_WORDS}).")
    else:
        lines.append(f"🔴 Мало: нужно ≥ {MIN_WORDS} слов.")
    lines.append("\n<i>Содержание и грамотность оценит преподаватель по критериям ФИПИ.</i>")
    return "\n".join(lines)
