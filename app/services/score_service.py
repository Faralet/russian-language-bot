from __future__ import annotations

"""Ориентир по баллу ЕГЭ на основе официальной шкалы перевода ФИПИ.

ВАЖНО (честная оговорка):
Это грубый учебный ориентир по точности ответов в тренажере, а НЕ официальный
прогноз результата ЕГЭ. Пока задания в боте не размечены по структуре заданий
ФИПИ (1-27) и их весу в первичных баллах, точный прогноз невозможен - он
появится на следующем этапе. Поэтому везде в интерфейсе число подается как
"ориентир", а не как "прогноз балла ЕГЭ".

Источник шкалы: официальная таблица перевода первичных баллов в тестовые,
ЕГЭ по русскому языку 2026 (максимум 50 первичных = 100 тестовых).
"""

# Тестовый балл для каждого первичного: индекс списка = первичный балл (0..50).
# Значения - по официальной шкале ЕГЭ-2026 по русскому языку.
PRIMARY_TO_TEST: tuple[int, ...] = (
    0, 3, 5, 8, 10, 12, 14, 17, 20, 22, 24, 27, 29, 32, 34, 36, 37, 39, 40, 42,
    43, 45, 46, 48, 49, 51, 52, 54, 55, 57, 58, 60, 61, 63, 64, 66, 67, 69, 70,
    70, 73, 75, 78, 81, 83, 86, 89, 91, 94, 97, 100,
)
MAX_PRIMARY = 50

# Минимум ответов в тренажере, прежде чем показывать ориентир (чтобы число
# не прыгало после двух вопросов).
MIN_ANSWERS_FOR_SCORE = 5

SCORE_DISCLAIMER = (
    "Это ориентир по твоей точности в тренажере, а не официальный прогноз ЕГЭ."
)


def accuracy_to_test_score(accuracy: float) -> int:
    """Грубый ориентир тестового балла (0..100) по доле верных ответов.

    Допущение: доля верных ответов в тренажере примерно равна доле первичных
    баллов, которую ученик набрал бы на всей работе (из 50 первичных). Это
    упрощение, поэтому результат подается как ориентир, а не прогноз.
    """
    accuracy = max(0.0, min(1.0, accuracy))
    primary = round(accuracy * MAX_PRIMARY)
    primary = max(0, min(MAX_PRIMARY, primary))
    return PRIMARY_TO_TEST[primary]


def score_line(correct: int, total: int) -> str | None:
    """Строка с ориентиром по баллу для интерфейса.

    Возвращает None, если данных пока мало (меньше MIN_ANSWERS_FOR_SCORE).
    """
    if total < MIN_ANSWERS_FOR_SCORE:
        return None
    accuracy = correct / total if total else 0.0
    score = accuracy_to_test_score(accuracy)
    return f"📈 Ориентир по баллу: ~<b>{score}</b> из 100"
