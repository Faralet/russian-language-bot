def _ex(topic, q, correct, wrong, short, full=None, example=None, fact=None,
        tags=None, level="basic", difficulty=2, exam="none"):
    options = [
        {"text": correct, "is_correct": True},
        {"text": wrong, "is_correct": False},
    ]
    # Правильный ответ не должен всегда стоять первым.
    if (len(q) + len(correct)) % 2 == 0:
        options.reverse()
    return {
        "topic_slug": topic,
        "level": level,
        "question": q,
        "options": options,
        "short_explanation": short,
        "full_explanation": full or short,
        "example_text": example,
        "interesting_fact": fact,
        "tags": tags or [],
        "difficulty_score": difficulty,
        "exam_type": exam,
    }


