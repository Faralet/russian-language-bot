def _q(topic, question, correct, wrongs, short, *, tags=None, level="exam", difficulty=3, exam="ege"):
    options = [{"text": correct, "is_correct": True}] + [{"text": w, "is_correct": False} for w in wrongs]
    shift = (len(question) + len(correct)) % len(options)
    options = options[shift:] + options[:shift]
    return {
        "topic_slug": topic, "level": level, "question": question, "options": options,
        "short_explanation": short, "full_explanation": None, "example_text": None,
        "interesting_fact": None, "tags": tags or [], "difficulty_score": difficulty,
        "exam_type": exam, "status": "published",
    }


