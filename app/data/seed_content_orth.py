def _q(topic, question, correct, wrong, short, *, tags=None, level="intermediate", difficulty=3, exam="ege"):
    options = [{"text": correct, "is_correct": True}, {"text": wrong, "is_correct": False}]
    if (len(question) + len(correct)) % 2 == 0:
        options.reverse()
    return {
        "topic_slug": topic,
        "level": level,
        "question": question,
        "options": options,
        "short_explanation": short,
        "full_explanation": None,
        "example_text": None,
        "interesting_fact": None,
        "tags": tags or [],
        "difficulty_score": difficulty,
        "exam_type": exam,
        "status": "published",
    }


